    private void adjustColumns(JTable table) {
        TableColumnModel columnModel = table.getColumnModel();
        for (int column = 0; column < table.getColumnCount(); column++) {
            int maxWidth = 0;
            DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
            centerRenderer.setHorizontalAlignment(JLabel.CENTER);
            for (int row = 0; row < table.getRowCount(); row++) {
                TableCellRenderer cellRenderer = table.getCellRenderer(row, column);
                Object value = table.getValueAt(row, column);
                Component comp = cellRenderer.getTableCellRendererComponent(table, value, false, false, row, column);
                maxWidth = Math.max(comp.getPreferredSize().width, maxWidth);
                table.getColumnModel().getColumn(column).setCellRenderer(centerRenderer);
            }
            TableColumn col = columnModel.getColumn(column);
            col.setPreferredWidth(maxWidth);
        }
    }
    
    private DefaultTableModel reverseTableModel(DefaultTableModel model) {
        DefaultTableModel reversedModel = new DefaultTableModel();
        
        for (int col = 0; col < model.getColumnCount(); col++) {
            reversedModel.addColumn(model.getColumnName(col));
        }
        
        for (int row = model.getRowCount() - 1; row >= 0; row--) {
            Object[] rowData = new Object[model.getColumnCount()];
            for (int col = 0; col < model.getColumnCount(); col++) {
                rowData[col] = model.getValueAt(row, col);
            }
            reversedModel.addRow(rowData);
        }
        
        return reversedModel;
    }
    
